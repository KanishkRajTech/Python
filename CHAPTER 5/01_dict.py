marks ={
    "Kanishk" : 100,
    "Prince" : 85,
    "Avi raj" : 45,
}
print(marks, type(marks))  # {'Kanishk': 100, 'Prince': 85, 'Avi raj': 45} <class 'dict'>

print(marks["Avi raj"])    # 45


